    function filtrar(texto) {
        const q = texto.toLowerCase().trim();
        items.forEach(el => {
            const nombre = el.dataset.nombre || '';
            el.classList.toggle('hidden', q !== '' && !nombre.includes(q));
        });
        actualizar();
    }

